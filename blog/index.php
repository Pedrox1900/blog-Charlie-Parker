                <p>O que fez de Charlie Parker um personagem tão fascinante? Ele nasceu em Kansas City em 29 de agosto de 1920. Como se diz, estava no lugar certo no momento certo. Aos 11 anos, ganhou de presente da mãe, que fez uma grande poupança, um saxofone alto. A ideia era tirar o garoto da tristeza que vivia pelo pai ter abandonado o lar.</p>
                <p>Nessa mesma época, ou seja, ainda criança, começou a fumar maconha. Aos 15 se casou e pela primeira vez começou a usar heroína. O casamento durou pouco, mas a dependência, a vida inteira. </p>
                <p>Em 1939, já tinha fama no Kansas quando decidiu ir para Nova York. Começou na cozinha de um restaurante lavando pratos, mas estava predestinado a brilhar no bairro do Harlem.</p>
                <p>Ao lado do trompetista Dizzy Gillespie, Parker iluminou esse gênero sincopado e improvisado. Com um toque pessoal, decidiu que era hora de quebrar as regras e foi então que surgiu o "bebop</p>
                <p>O gênero resumia sua vida: um caos que era reconduzido rumo à beleza. Começaram então suas grandes apresentações, como as célebres "Dial Sessions" e suas grandes composições: "Yardbird Suite", "Ornithology" e "Bird of Paradise", que acabou lhe rendendo o merecido apelido.</p>
                <p>Mas Parker era uma ave de passagem. O reverso obscuro de seu sucesso passava por clínicas de desintoxicação, quartos de hotel destruídos em plena bebedeira e uma ruína econômica que lhe deu o golpe final, quando sua filha Pree morreu por uma fibrose cística cujo tratamento o artista não pôde pagar.</p>
                <p>Charlie Parker faleceu no Standhope Hotel, em Nova York. A autópsia apontou pneumonia, úlcera e um avançado estado de cirrose, somados a um ataque cardíaco. O médico legista, que não sabia de quem se tratava, descreveu o corpo inerte como o de um homem de 50 ou 60 anos, muito mais do que sua real idade.</p>
