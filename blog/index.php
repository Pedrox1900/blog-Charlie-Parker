                <h2 class="texto-direita">Obra</h2>
                <img class="esquerda" src="imagens/charlie3.jpg" alt="obracharlie">
                <p class="texto-direita">Foi no final dos anos 1930 e início dos anos 1940 que ele criou os primeiros solos notáveis em "Oh, Lady Be Good" e "Honeysuckle Rose". O encontro que mudaria sua vida para sempre foi quando conheceu Dizzy Gillespie. Em poucos anos, a dupla lançaria gravações históricas no jazz, como os "duelos" históricos em faixas como "Groovin 'High", "Dizzy Atmosphere", "Shaw' Nuff", "Salt Peanuts" e "Hot House".</p>
                <p class="texto-direita">O bebop não era uma revolução musical como foi o rock uma década depois ou como o punk 20 anos depois do lançamento do primeiro álbum de estúdio de Elvis Presley. Esse subgênero do jazz abalou as estruturas da música como um todo em um período perfeito para isso acontecer e o gênero não foi mais o mesmo ao entrar em uma segunda era de ouro.</p>
                <p class="texto-direita">No final de 1948, Parker começaria o histórico período na gravadora Mercury através da parceria com Norman Granz, fundador do selo Clef. O produtor havia acabado de colocar o projeto do selo na praça quando conseguiu o saxofonista em seu elenco inicial. Enquanto lutava para ter mais artistas, contar com Parker foi uma benção, pois o saxofonista queria fazer coisas diferentes e nem sempre optava por gravar com sua banda principal.</p>
                <p class="texto-direita">Foi disso que surgiu a ideia de gravar clássicos dos jazz acompanhado por um conjunto de cordas com um grupo muito diferente de qualquer coisa que ele já havia feito em toda carreira. Muitos críticos da época alegaram que era um álbum "comercial demais". E era mesmo. Mas, mesmo nascido para dar dinheiro a todos, fez bastante sucesso com a ousada mistura em apresentar o bebop com sopros, harpa, coro e uma banda.</p>
